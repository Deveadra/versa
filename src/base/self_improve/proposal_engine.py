PROPOSAL_SYS_PROMPT = """You are Aerith's code proposer.
Given the user's natural-language request and a summary of the repository,
propose a *minimal, safe patch set*.

Return a JSON object with:
{{
  "title": "...",
  "description": "...",
  "changes": [
    {{
      "path": "relative/path.py",
      "apply_mode": "replace_block" | "full_file",
      "search_anchor": "...",
      "replacement": "..."
    }},
    ...
  ]
}}

Rules:
- Never use "full_file" for an existing file. Only use "full_file" for new files.
- For existing files, always use "replace_block" with a search_anchor copied exactly from the file.
- Only modify files within the allowlist.
- Use "full_file" ONLY for new files. For existing files, use "replace_block".
- Keep changes small; do not exceed {max_files} files or {max_bytes} bytes total replacement.
- Prefer 'replace_block' with a reliable 'search_anchor' snippet to find the place to change.
- NEVER include secrets or tokens.
"""


def _parse_llm_json(raw: str) -> dict:
    """
    Best-effort JSON parser for LLM responses.
    Handles fenced blocks, leading/trailing commentary, and trailing commas.
    Falls back to ast.literal_eval for python-dict-ish outputs.
    """
    if not raw:
        return {}

    s = raw.strip()

    # Strip markdown fences if present
    if s.startswith("```"):
        s = re.sub(r"^```[a-zA-Z0-9_-]*\s*\n", "", s).strip()
        s = re.sub(r"\n```$", "", s).strip()

    # First attempt: strict JSON
    try:
        obj = json.loads(s)
        return obj if isinstance(obj, dict) else {}
    except Exception:
        pass

    # Second attempt: extract the largest {...} block
    l = s.find("{")
    r = s.rfind("}")
    if 0 <= l < r:
        blob = s[l : r + 1].strip()

        # Remove common trailing commas (best-effort)
        blob2 = re.sub(r",\s*([}\]])", r"\1", blob)

        try:
            obj = json.loads(blob2)
            return obj if isinstance(obj, dict) else {}
        except Exception:
            pass

        # Last resort: python literal dict (single quotes etc.)
        try:
            obj = ast.literal_eval(blob2)
            return obj if isinstance(obj, dict) else {}
        except Exception:
            return {}

    return {}


def _ask_for_proposal_json(self, *, user_prompt: str, system_prompt: str) -> tuple[str, dict]:
    """
    Ask the LLM for proposal JSON, retrying once if the first response isn't valid JSON.
    Returns (raw_text, parsed_obj_dict).
    """
    raw1 = self.brain.ask_brain(user_prompt, system_prompt=system_prompt).strip()
    obj1 = _parse_llm_json(raw1)
    if obj1:
        return raw1, obj1

    # Retry with an explicit "repair" prompt (smaller, more forceful)
    repair_prompt = f"""Your previous response was NOT valid JSON.

Return ONLY a valid JSON object that matches the required schema. No commentary, no markdown fences.

Schema reminder:
{{
  "title": "...",
  "description": "...",
  "changes": [
    {{
      "path": "relative/path.py",
      "apply_mode": "replace_block" | "full_file",
      "search_anchor": "...",
      "replacement": "..."
    }}
  ]
}}

Previous response:
{raw1}
"""
    raw2 = self.brain.ask_brain(repair_prompt, system_prompt=system_prompt).strip()
    obj2 = _parse_llm_json(raw2)
    if obj2:
        return raw2, obj2

    # Give back the original for logging/debugging
    return raw1, {}


class ProposalEngine:
    def __init__(self, repo_root: str, brain: Brain | None = None):
        self.root = Path(repo_root).resolve()
        self.brain = brain or Brain()

    def _read(self, relpath: str) -> str:
        return (self.root / relpath).read_text(encoding="utf-8", errors="ignore")

    def _validate_change_path(self, relpath: str) -> tuple[bool, str, str]:
        """
        Enforce hard safety boundaries on what Aerith can modify.
        Returns (allowed, normalized_path, reason_if_denied).
        """
        rp = Path(relpath or "").as_posix().lstrip("/")

        if not rp or rp.startswith("..") or "/.." in rp:
            return False, rp, "path traversal or empty path"

        # Hard deny list (protect repo config)
        if rp == "pyproject.toml":
            return False, rp, "pyproject.toml is protected"

        # Allow-list (default: only src/)
        allowed_prefixes = getattr(settings, "proposer_allowed_prefixes", ["src/"])
        allowed_exact = set(getattr(settings, "proposer_allowed_exact_paths", []))
        # Allow the one auto-test file if you still want it
        allowed_exact.add("tests/test_autogenerated.py")

        if rp in allowed_exact:
            return True, rp, ""

        if any(rp.startswith(p.rstrip("/") + "/") for p in allowed_prefixes):
            return True, rp, ""

        return False, rp, "outside allowed prefixes"

    def _ask_for_proposal_json(self, *, user_prompt: str, system_prompt: str) -> tuple[str, dict]:
        """
        Ask the LLM for proposal JSON, retrying once if the first response isn't valid JSON.
        Returns (raw_text, parsed_obj_dict).
        """
        raw1 = self.brain.ask_brain(user_prompt, system_prompt=system_prompt).strip()
        obj1 = _parse_llm_json(raw1)
        if obj1:
            return raw1, obj1

        # Retry with an explicit "repair" prompt (smaller, more forceful)
        repair_prompt = f"""Your previous response was NOT valid JSON.

    Return ONLY a valid JSON object that matches the required schema. No commentary, no markdown fences.

    Schema reminder:
    {{
    "title": "...",
    "description": "...",
    "changes": [
        {{
        "path": "relative/path.py",
        "apply_mode": "replace_block" | "full_file",
        "search_anchor": "...",
        "replacement": "..."
        }}
    ]
    }}

    Previous response:
    {raw1}
    """
        raw2 = self.brain.ask_brain(repair_prompt, system_prompt=system_prompt).strip()
        obj2 = _parse_llm_json(raw2)
        if obj2:
            return raw2, obj2

        # Give back the original for logging/debugging
        return raw1, {}

    def safe_write(self, path: str, new_content: str) -> bool:
        """
        Safely write new content to file.
        - Writes to a temp file first
        - Diffs against existing (if file exists)
        - Replaces original only if content changed
        """
        try:
            original = ""
            p = Path(path)
            if p.exists():
                original = p.read_text(encoding="utf-8", errors="ignore")

            diff = list(
                difflib.unified_diff(
                    original.splitlines(),
                    new_content.splitlines(),
                    fromfile="before",
                    tofile="after",
                )
            )
            if not diff:
                return False  # no changes

            fd, tmp_path = tempfile.mkstemp()
            with os.fdopen(fd, "w", encoding="utf-8") as tmpf:
                tmpf.write(new_content)

            p.parent.mkdir(parents=True, exist_ok=True)
            shutil.move(tmp_path, path)
            return True
        except Exception as e:
            logger.error(f"Safe write failed for {path}: {e}")
            return False

    def _apply_change(self, change: ProposedChange) -> tuple[bool, str]:
        """
        Apply a ProposedChange safely.

        Modes:
        - full_file:
            * backup original into .aerith/backups/<relpath>.bak (gitignored)
            * overwrite target file
        - replace_block:
            * if file missing -> create new with replacement
            * if anchor present and found -> replace first occurrence
            * if anchor missing/empty or not found -> do NOT rewrite entire file (fail safely)
        """
        try:
            target = (self.root / change.path).resolve()
            target.parent.mkdir(parents=True, exist_ok=True)

            mode = getattr(change, "apply_mode", None) or "replace_block"
            replacement = change.replacement or ""
            anchor = getattr(change, "search_anchor", None) or ""

            # ---- full_file (safe overwrite) ----
            if mode == "full_file":
                # Refuse full-file overwrites of existing files unless explicitly allowed.
                allow_overwrite = bool(
                    getattr(settings, "proposer_allow_full_file_overwrite", False)
                )
                if target.exists() and not allow_overwrite:
                    msg = (
                        f"full_file refused for existing file '{target}': overwrites are disabled. "
                        "To allow this, set settings.proposer_allow_full_file_overwrite=True "
                        "(or configure it via your environment settings)."
                    )
                    logger.warning(f"[proposal_engine] {msg}")
                    return False, msg

                backups = self.root / ".aerith" / "backups"
                backups.mkdir(parents=True, exist_ok=True)

                if target.exists():
                    rel = target.relative_to(self.root).as_posix().replace("/", "__")
                    backup_path = backups / f"{rel}.bak"
                    try:
                        backup_path.write_text(
                            target.read_text(encoding="utf-8", errors="ignore"), encoding="utf-8"
                        )
                    except Exception:
                        # best-effort backup only
                        pass

                original = (
                    target.read_text(encoding="utf-8", errors="ignore") if target.exists() else ""
                )
                if original == replacement:
                    return False, "no diff (full_file)"

                target.write_text(replacement, encoding="utf-8")
                logger.info(f"[apply_change] full_file → wrote {target}")
                return True, "full_file write"

            # ---- replace_block ----
            if not target.exists():
                target.write_text(replacement, encoding="utf-8")
                logger.info(f"[apply_change] created new file {target}")
                return True, "created new file"

            if not anchor.strip():
                # Safety: do not allow empty-anchor rewrite of existing files
                return False, "replace_block refused: empty anchor for existing file"

            original = target.read_text(encoding="utf-8", errors="ignore")
            if anchor not in original:
                return False, "replace_block refused: anchor not found"

            new_content = original.replace(anchor, replacement, 1)
            if new_content == original:
                return False, "no diff after anchor replace"

            target.write_text(new_content, encoding="utf-8")
            logger.info(f"[apply_change] block replaced in {target}")
            return True, "block replaced"

        except Exception as e:
            logger.exception(f"[apply_change] error for {change.path}: {e}")
            return False, f"error: {e}"

    def propose(self, instruction: str, index_md: str) -> Proposal:
        sys_prompt = PROPOSAL_SYS_PROMPT.format(
            max_files=settings.proposer_max_files_per_pr,
            max_bytes=settings.proposer_max_patch_bytes,
        )
        user_prompt = f"""User request:
                        {instruction}

                        Repository index:
                        {index_md}

                        Respond with strictly the JSON schema described.
                        """
        # raw = self.brain.ask_brain(user_prompt, system_prompt=sys_prompt).strip()
        force_nonempty = getattr(settings, "proposer_force_nonempty", False)

        raw, obj = self._ask_for_proposal_json(user_prompt=user_prompt, system_prompt=sys_prompt)
        if not obj:
            logger.error("LLM returned invalid JSON twice; wrapping into no-op change.")
            obj = {
                "title": f"Aerith Proposal: {instruction[:40]}",
                "description": raw,
                "changes": [],
            }

        # Auto add a tiny test if the change looks substantive
        auto_test_needed = any(
            len(ch.get("replacement", "").splitlines()) > 15 or "def " in ch.get("replacement", "")
            for ch in obj.get("changes", [])
        )
        if auto_test_needed:
            obj.setdefault("changes", []).append(
                {
                    "path": "tests/test_autogenerated.py",
                    "apply_mode": "full_file",
                    "search_anchor": None,
                    "replacement": "def test_autogenerated():\n    assert True\n",
                }
            )

        changes: list[ProposedChange] = [
            ProposedChange(
                path=ch.get("path", ""),
                apply_mode=ch.get("apply_mode", "replace_block"),
                search_anchor=ch.get("search_anchor"),
                replacement=ch.get("replacement", ""),
            )
            for ch in obj.get("changes", [])
        ]

        normalized: list[ProposedChange] = []
        for ch in changes:
            target = self.root / ch.path
            if ch.apply_mode == "full_file" and target.exists():
                # Convert to a safe block replace:
                # anchor is the first ~15 lines of the current file (stable enough for one iteration)
                try:
                    original = target.read_text(encoding="utf-8", errors="ignore")
                    anchor_lines = original.splitlines()[:15]
                    anchor = "\n".join(anchor_lines).strip()
                except Exception:
                    anchor = ""

                if anchor:
                    ch.apply_mode = "replace_block"
                    ch.search_anchor = anchor
                    # replacement stays as the model proposed (whole file). It will replace the anchored header block only.
                    # That *still* might not be what we want, but it converts a guaranteed-noop into a real attempt.
                else:
                    # If we can't build an anchor, keep it as-is (it will be refused)
                    pass

            normalized.append(ch)

        changes = normalized

        if not changes and force_nonempty:
            changes.append(
                ProposedChange(
                    path="tests/test_autogenerated.py",
                    apply_mode="full_file",
                    search_anchor=None,
                    replacement="def test_aerith_placeholder():\n    assert True\n",
                    rationale="Ensure non-empty PR for CI visibility when no concrete changes inferred.",
                )
            )

        return Proposal(
            title=obj.get("title", f"Aerith proposal: {instruction[:30]}"),
            description=obj.get("description", ""),
            changes=changes,
        )

    def preflight_proposal(self, proposal: Proposal) -> tuple[list[ProposedChange], list[dict]]:
        """
        Determine which changes are actually applicable *before* we create a branch.
        Returns: (applicable_changes, refusals)
        - applicable_changes: changes that should apply cleanly under current safety rules
        - refusals: list of {"path": ..., "apply_mode": ..., "reason": ...} for each rejected change
        """
        applicable: list[ProposedChange] = []
        refusals: list[dict] = []

        for ch in proposal.changes:
            try:
                rel = (ch.path or "").strip()
                if not rel:
                    refusals.append(
                        {"path": rel, "apply_mode": ch.apply_mode, "reason": "empty path"}
                    )
                    continue

                target = (self.root / rel).resolve()

                mode = getattr(ch, "apply_mode", None) or "replace_block"
                anchor = getattr(ch, "search_anchor", None) or ""
                replacement = ch.replacement or ""

                # full_file: allowed only for new files unless explicitly enabled
                if mode == "full_file":
                    if target.exists() and not getattr(
                        settings, "proposer_allow_full_file_overwrite", False
                    ):
                        refusals.append(
                            {
                                "path": rel,
                                "apply_mode": mode,
                                "reason": "full_file refused: overwrite disabled",
                            }
                        )
                        continue
                    if not replacement:
                        refusals.append(
                            {"path": rel, "apply_mode": mode, "reason": "empty replacement"}
                        )
                        continue
                    applicable.append(ch)
                    continue

                # replace_block rules
                if not target.exists():
                    # creating a new file is fine
                    if not replacement:
                        refusals.append(
                            {"path": rel, "apply_mode": mode, "reason": "empty replacement"}
                        )
                        continue
                    applicable.append(ch)
                    continue

                # existing file requires non-empty anchor that exists in file
                if not anchor.strip():
                    refusals.append(
                        {
                            "path": rel,
                            "apply_mode": mode,
                            "reason": "replace_block refused: empty anchor",
                        }
                    )
                    continue

                original = target.read_text(encoding="utf-8", errors="ignore")
                if anchor not in original:
                    refusals.append(
                        {
                            "path": rel,
                            "apply_mode": mode,
                            "reason": "replace_block refused: anchor not found",
                        }
                    )
                    continue

                # would replace, so it's applicable
                applicable.append(ch)

            except Exception as e:
                refusals.append(
                    {
                        "path": getattr(ch, "path", ""),
                        "apply_mode": getattr(ch, "apply_mode", None),
                        "reason": f"preflight error: {e}",
                    }
                )

        return applicable, refusals

    def apply_proposal(self, proposal: Proposal) -> list[tuple[ProposedChange, bool, str]]:
        """
        Apply proposed changes to the working tree.
        Git branching is handled by Orchestrator/PRManager.
        """
        applied: list[tuple[ProposedChange, bool, str]] = []
        total_bytes = 0

        for ch in proposal.changes[: settings.proposer_max_files_per_pr]:
            ok_path, norm_path, reason = self._validate_change_path(ch.path)
            if not ok_path:
                applied.append((ch, False, f"refused: {reason}"))
                continue
            # Normalize in case caller used weird slashes
            ch.path = norm_path

            total_bytes += len(ch.replacement.encode("utf-8"))
            if total_bytes > settings.proposer_max_patch_bytes:
                applied.append((ch, False, "patch byte limit exceeded"))
                continue

            ok, msg = self._apply_change(ch)
            applied.append((ch, ok, msg))

            if ok:
                lines_changed = len(ch.replacement.splitlines())
                if lines_changed > 15 or "def " in ch.replacement:
                    self._generate_test_stub(ch.path, ch.replacement)

        return applied

    def _generate_test_stub(self, path: str, replacement: str) -> None:
        test_dir = self.root / "tests"
        test_dir.mkdir(exist_ok=True)
        module_name = Path(path).stem
        test_file = test_dir / f"test_{module_name}.py"
        if test_file.exists():
            return
        content = """def test_placeholder():
    assert True
"""
        test_file.write_text(content, encoding="utf-8")
